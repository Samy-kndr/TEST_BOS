public class User {

    String name;
    int workingHours;


    public void setName(String name){
        this.name = name;
    }

    public String getName(){
        return this.name;
    }

    public void setWorkingHours(int workingHours){
        this.workingHours = workingHours;
    }

    public int getWorkingHours(){
        return this.workingHours;
    }

}
